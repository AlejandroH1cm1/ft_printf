#include "libpf.h"

void	cleanse_str(char **s)
{
	char *tmp;

	if ((s[0][0] < '0' || s[0][0] > '9') && (s[0][0] < 'a' || s[0][0] > 'z')
			&& (s[0][0] < 'A' || s[0][0] > 'Z'))
	{
		tmp = ft_strdup(&(s[0][1]));
		free(*s);
		*s = tmp;
	}
}
